package Chapter4;

public class MathemeticalFunctions {
    public static void main(String[] args){

        // trigonometry
        System.out.println(Math.sin(Math.toRadians(30)));
        System.out.println(Math.asin(0.5));
        System.out.println(Math.sin(Math.PI / 6));
        System.out.println(Math.toRadians(30));
        System.out.println(Math.toDegrees(0.5));

        // exponent
        System.out.println(Math.exp(2));
        System.out.println(Math.log(Math.exp(2)));
        System.out.println(Math.log10(100));
        System.out.println(Math.sqrt(25));

        // rounding
        System.out.println(Math.ceil(15.90));
        System.out.println(Math.floor(15.90));
        System.out.println(Math.rint(15.90));
        System.out.println(Math.round(15.90));

        // min, max, abs

        // random

        
    }
}
